""" Slicing a list in python """

list=['a','b','c','d','e']
print(list[2:4])

""" Slicing from an index to the end """

list=['a','b','c','d','e']
print(list[2:])

""" Slicing from the start to a specific index """

list=['a','b','c','d','e']
print(list[:4])

""" Increment by a number while slicing between a range of items in a list """

list=['a','b','c','d','e','f','g','h']
print(list[1:6:2])
